--- MÜÜGIKANALITE ANALÜÜS ---
Füüsiline pood toob ettevõttele ülekaalukalt enim müüke (1,9 miljonit eurot) ning samuti kõige rohkem kliente (2278 klienti). 
Pood on ka selgelt kõige efektiivsem kanal, tuues keskmiselt 835€ kliendi kohta, samas kui online-kanalis on see näitaja 590€. 
Linnade lõikes joonistub välja kindel muster: nii poes kui ka veebis teevad suurima käibe suurlinnad (Tallinn, Tartu, Pärnu), 
kuid ka väiksemate linnade elanikud eelistavad hetkel veel tugevalt füüsilist poodi. Konkreetne soovitus on rakendada e-poes 
agressiivsemaid lisamüügi strateegiaid, et tõsta online-kanali efektiivsust ja jõuda lähemale füüsilise poe keskmisele klienditulule.

--- ÄRILISED JÄRELDUSED JA SOOVITUSED ---
1. Kaupluste ja kanalite jaotus:
   Tallinna, Tartu ja Pärnu kaupluste trend on sarnane – füüsiliste poodide käive on e-poest kordades suurem. 
   Tallinn genereerib nii füüsilises poes kui ka veebis suurima osa ettevõtte käibest (pood: 1,09 mln €, online: 335k €). 

2. Kas mõni kauplus peaks rohkem online-müügile panustama?
   Jah, eelkõige peaksid online-müügi turundusele fookuse seadma Tartu ja Pärnu, kus digikanali osakaal on võrreldes 
   potentsiaaliga madal. Samuti on väiksemates linnades (nt Narva, Viljandi, Rakvere) online-müük praegu tagasihoidlik – 
   siin on kasutamata turg, kuhu saaks läbi e-poe kergesti laieneda ilma füüsilist poodi ehitamata.

3. Kuhu peaks Anna turunduseelarvet suunama?
   * E-poe keskmise ostukorvi kasvatamisele: Kuna kliendid on veebis juba olemas, tuleks eelarvet kasutada kampaaniateks, 
   mis meelitavad rohkem kulutama (nt "osta X€ eest, saad tasuta tarne/kingituse" või kampaaniad tootekategooriatele, mis 
   poes on populaarsed, aga veebis mitte).
   * Suunatud digiturundus väiksematesse linnadesse: Kuna poodides tehakse suurim käive Tallinnas/Tartus/Pärnus, peaks 
   digikampaaniad sihtima geograafiliselt just regioonidesse, kus füüsiline pood on kaugemal.
   * Omnichannel kogemus: Suunata turunduseelarvet lahendustesse, mis seovad kanalid (nt poest ostnud kliendile sooduskood 
   e-poodi), et suunata püsikliente kasutama mõlemat platvormi.

