 1. KÕIGE EDUKAMAD KATEGOORIAD (MÜÜGITULU LIIDRID)
 -------------------------------------------------------------------------------------------
  • Finantstulemuste põhjal on ettevõtte kindlad müügihitid ja käibemootorid JALANÕUD 
    (774 034,75 €) ja MEESTE RIIDED (749 798,72 €). 
  • Kuna need kaks kategooriat moodustavad lõviosa kogutulust, tuleb tagada nende tooterühmade 
    püsiv ja katkematu tarnekindlus ning hoida neid turunduskampaaniate fookuses.


 2. MÜÜMATA TOODETE ELIMINEERIMINE JA LAO OPTIMEERIMINE
 -------------------------------------------------------------------------------------------
  • SQL-analüüs tuvastas andmebaasist täpselt 12 TOODET, MIDA POLE KUNAGI MÜÜDUD 
    (mille puhul 'sales.sale_id IS NULL').
  • Kallihinnalise laopinna vabastamiseks ja seisva kapitali minimeerimiseks on soovitatav 
    need tooted püsivast tootesortimendist täielikult eemaldada või realiseerida need kiiresti 
    allahindluste/kampaaniate kaudu.


 3. JUURDETELLIMISE STRATEEGIA JA TARNEAHELA JUHTIMINE
 -------------------------------------------------------------------------------------------
  • Kõik tooted, mille praegune laoseis on langenud alla või võrdseks kriitilise piiriga 
    (staatus 'TELLI JUURDE'), vajavad kohest ostuosakonna sekkumist ja varude täiendamist.
  • Tellimuste tegemisel tuleb prioriteediks seada absoluutsed top-müügihitid, nagu näiteks:
      - "Õhuline sünteetiline sporditossud" (müüdud 35 korda, kogumüük 27 347,04 €)
      - "Trendikas goretex oxfordid"       (müüdud 32 korda, kogumüük 23 376,15 €)
    See tagab, et kõige tulusamad tooted ei satuks laodefitsiiti (Stockout).

