[KASUMLIKUMAD KATEGOORIAD]
Kõige kasumlikumaks osutuvad jalanõud ja meeste riided, mis toovad tänu kõrgele 
keskmisele hinnale (vastavalt 214.10 € ja 189.91 €) ning suurtele müügikogustele 
