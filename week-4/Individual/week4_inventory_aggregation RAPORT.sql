[TÄHELEPANU VAJAVAD TOOTED]
Tähelepanu vajavad aga laste riided ja teatud naiste riiete hitid (näiteks naiste 
siidised cargo püksid ja laste softshell komplekt), mis müüvad küll tükiliselt 
tohutult hästi, kuid jätavad madala jaehinna tõttu ettevõttele 
ebaproportsionaalselt väikese rahalise tulu. 

[SOOVITUSED]
